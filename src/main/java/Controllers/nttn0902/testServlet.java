    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        Connection conn = null;
        List<Event> events = new ArrayList<>();

        try {
            conn = DBContext.getInstance().getConnection();
            String sql = "SELECT EventID, EventName, StartDate, EndDate, ImageURL FROM Events";
            PreparedStatement ps = conn.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();

            while (rs.next()) {
                Event e = new Event();
                e.setEventID(rs.getInt("EventID"));
                e.setEventName(rs.getString("EventName"));
                e.setStartDate(rs.getDate("StartDate"));
                e.setEndDate(rs.getDate("EndDate"));
                e.setImageURL(rs.getString("ImageURL"));
                events.add(e);
            }

            request.setAttribute("events", events);
            request.getRequestDispatcher("/OrderManagement/test.jsp").forward(request, response);

        } catch (Exception e) {
            e.printStackTrace();
            response.getWriter().println("Error: " + e.getMessage());
        }
    }

